void era() {
	f[0] = f[1] = false;
	for(int i = 2; i*i <= L; ++i) 
		if(f[i]) for(int j = i*i; j <= L; j += i) f[j] = false; 

	prime.pb(2);
	for(int i = 3; i <= L; i += 2) if(f[i]) prime.pb(i);
}

bool check(ll a) {
	if(a < 2) return false;
	if(a == 2) return true;
	if(a%2 == 0) return false;
	for(ll i = 3; i*i <= a; i += 2) if(a%i == 0) return false;
	return true;
}

